#diketahui array mobil
mobil = ["Toyota", "Honda", "Suzuki"]

# mengambil nilai array tertentu
x = mobil [0]
print(x)

print("=======================")
# Looping array
for y in mobil:
    print(y)

print("=======================")
# menambah elemen pada array
mobil.append("Isuzu")
print(mobil)

print("=======================")
# menghapus elemen array
mobil.remove("Suzuki")
print(mobil)
